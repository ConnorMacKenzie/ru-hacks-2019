const GET_QUERY = ggl`
{
  sellers(limit:2){
  sellerid
  products(limit:2){
    productcode
    orders(limit:2){
      total_qty
      order_price
      audit_mth
      buyer{
        buyerid
      }
    }
  }
}
}`;

const orderPriceWithBuyerFacts= ggl`{
	ordersByAuditMths(audit_mth:["jan-17","feb-17","mar-17","apr-17"], limit:1000
  ){
    order_price
  	audit_mth	
    buyer{
      buyerid
    }
  }
  `;

const query = () => (
    <Query query={GET_QUERY}>
      {({data: {sellers}, loading}) => {
        if (loading || !sellers){
          return <div id="chartLoading">Loading...</div>;
        }
        return (
            <SellersList sellers={sellers} />
        );
      }}
    </Query>
);

const SellersList = ({ sellers }) => (
    <ul>
      {sellers.map(node => {
        return (
            <li key={node.sellerid}>
              {node.products.map(p => (
                  <li key={p.productcode}>
                    <p>{p.productcode}</p>
                  </li>
              ))}
              <p>{node.id}</p>
            </li>
        );
      })}
    </ul>
);
